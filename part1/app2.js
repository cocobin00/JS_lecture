// data type

// Primitive

// 1. Number
console.log(10);
let num = 12;
console.log(num);
console.log(typeof num);

// 2. String
console.log("kevin");
let name = "John";
console.log(name);
console.log(typeof name);

// template literal (ES6)
let myName = "kim";
let templateLiteral = `hello ${myName}`;
console.log(templateLiteral);

// 3. Boolean
console.log(true);
let isFun = false;
console.log(isFun);
console.log(typeof isFun);

// 4. Undefined
let apple;
console.log(apple);
console.log(typeof apple);

// 5. Null
let time = null;
console.log(time);
console.log(typeof time);

// 6. Symbol (ES6 = ES2015)
let symbol = Symbol();
console.log(symbol);
console.log(typeof symbol);

// 7. BigInt (ES2020)
theBiggestInt = 9007199254343443;
console.log(theBiggestInt);
console.log(typeof theBiggestInt);
